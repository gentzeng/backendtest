package de.bringmeister.model;

public class Price {
	private Double value;
	private String currency;

	public Double getValue() {
		return value;
	}

	public void setValue(Double value) {
		value = value;
	}

	public String getCurrency() {
		return currency;
	}

	public void setCurrency(String currency) {
		currency = currency;
	}

}
